<section class="description p-4">
	<h2 class="text-xl font-semibold mb-2">Audio Tool Description</h2>
	<p>Taga11y is a proof of concept audio labeling tool for visually impaired people.</p>
	<h2 class="text-lg font-semibold mt-4 mb-2">Shortcuts</h2>
	<ul class="list-disc list-inside">
		<li><b>Space (spacebar):</b> Toggle play/pause of the audio.</li>
		<li><b>H (letter H):</b> Press and hold to pause audio; release to resume playback.</li>
		<li>
			<b>[ (left square bracket) and ] (right square bracket):</b> Adjust the grid size for more or less
			fine grained scrubbing. Also changes playback speed.
		</li>
		<li><b>= (equal sign):</b> Reset the grid size to 1.</li>
		<li>
			<b>, (comma) and . (fullstop):</b> Seek backward or forward based on the current grid size.
		</li>
		<li>
			<b>Shift + , (Shift and comma) and Shift + . (Shift and fullstop):</b> Seek to the beginning or
			end of the audio.
		</li>
		<li><b>C (letter C):</b> Announce the current time of the audio.</li>
		<li><b>G (letter G):</b> Announce the current grid size.</li>
		<li><b>E (letter E):</b> Add an event annotation at the current time.</li>
		<li>
			<b>S (letter S):</b> Set or complete a timestamp annotation, which has a start and end point. First
			keypress sets a partial timestamp, the second keypress finalizes the timestamp.
		</li>
	</ul>
</section>
